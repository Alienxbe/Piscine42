int		verif_possible(int *input)
{
	int i;

	i = 0;
	while (i < 12)
	{
		if (!((( input[i] == 4) && ( input[i + 4] == 1)) || \
				(( input[i] == 3) && ( input[i + 4] == 2)) || \
				(( input[i] == 3) && ( input[i + 4] == 1)) || \
				(( input[i] == 2) && ( input[i + 4] == 1)) || \
				(( input[i] == 2) && ( input[i + 4] == 2)) || \
				(( input[i] == 2) && ( input[i + 4] == 3)) || \
				(( input[i] == 1) && ( input[i + 4] == 2)) || \
				(( input[i] == 1) && ( input[i + 4] == 3)) || \
				(( input[i] == 1) && ( input[i + 4] == 4))))
		{
			return (0);
		}
		if (i == 3)
			i += 4;
		i++;
	}
	return (1);
}
