#include <unistd.h>

void	ft_putstr(char *str)
{
	int i;
	int c;

	i = 1;
	c = 0;
	while (i == 1)
	{
		if (str[c] == 0)
		{
			i = 0;
		}
		else
		{
			write(1, str + c, 1);
		}
		c++;
	}
}
