char	*ft_strcpy(char *dest, char *src)
{
	int i;
	int c;

	i = 1;
	c = 0;
	while (i == 1)
	{
		dest[c] = src[c];
		if (src[c] == 0)
		{
			i = 0;
		}
		c++;
	}
	return (dest);
}
