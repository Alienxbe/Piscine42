int		ft_str_is_uppercase(char *str)
{
	int c;
	int out;

	c = 0;
	out = 1;
	while (str[c] != 0)
	{
		if (!(str[c] > 64 && str[c] < 91))
			out = 0;
		c++;
	}
	return (out);
}
