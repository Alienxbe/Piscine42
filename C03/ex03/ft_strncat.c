char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int c;

	c = -1;
	while (dest[++c])
	{
	}
	while (*src && nb--)
		dest[c++] = *(src++);
	dest[c] = 0;
	return (dest);
}
